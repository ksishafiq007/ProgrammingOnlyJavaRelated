package programming;

public class MathAPIExamples {
	public static void main(String[] args) {
		int a = 10;
		int b = -19;
		double x = 152.3;
		double y = 0.349;
		//absolute value
		System.out.println("|" + a + "| is " + Math.abs(a));
		System.out.println("|" + b + "| is " + Math.abs(b));
		System.out.println("|" + x + "| is " + Math.abs(x));
		System.out.println("|" + y + "| is " + Math.abs(y));
	}

}
